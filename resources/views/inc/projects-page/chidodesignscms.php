            <h4 class="mt-1 blue-text">App Overview</h4>

            <p class="mg-btm-4">
                This application is built in Laravel 7, Vue.js 3 & Bootstrap 4. It is an api focused application, with user authentication being the focal point of the build. The application enables the privatization of articles, only allowing users to view their own content.
                <br><br>
                The application from a frontend perspective is broken down into reusable components via Vue.js and fundamentally adheres to SPA architecture. We use axios to make HTTP calls to our Laravel API and process response data accordingly.
